<h4>About this project</h4>
    <p>This project grew out of a general interest in alternative archiving practices and community archiving. In recent years, it seems there has been a growing interest on the part of memory insitutions in collecting and preserving signs and other ephemera that are created during protest movements. These objects are important to preserve, as they have the ability to offer a unique window into the politics, hopes, fears and emotions of a community at a specific point in time. However, there are some inherent tensions involved when these items are collected by and housed within major institutions that already have outsized power over the shaping of cultural narratives. This project is not an attempt to resolve any of these extremely complex issues, but instead is an experiment in creating a community archive of protest imagery for one specific city and time period that is open to whoever wishes to use it. </p><br><br><br>
    
